-- Create view for lecture analysis (simplified without user info)
CREATE VIEW lecture_analysis_view AS
SELECT l.title                  AS lecture_title,
       COUNT(df.defect_type_id) AS defects_found,
       AVG(df.probability)      AS avg_confidence,
       MAX(ar.analysis_date)    AS last_analysis
FROM analysis_results ar
         JOIN lectures_ai l ON ar.lecture_id = l.id
         LEFT JOIN defect_findings df ON ar.id = df.analysis_id
GROUP BY ar.id, l.title;
